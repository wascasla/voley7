                                <p>
                                    REGLAMENTO LIGA DEL VALLE</p>
<p class="letraBlanca">Pauta 1: Seña</p>
<p>
Aquellos equipos preseleccionados por la Organización de Liga Del Valle, tendrán como requerimiento para ingresar a la nómina de equipos del Torneo, la realización de un adelanto en concepto de “seña” correspondiente a la suma designada por la Organización. Lo abonado será considerado como pago parcial a cuenta de lo adeudado en concepto de inscripción.
</p>
<p>
Pauta 2: Inscripción al torneo
</p>
<p>
Los respectivos equipos integrantes del torneo deberan cumplir en forma obligatoria con la prestacion dineraria equivalente a "incripcion". El monto total a abonar sera de $1800, debiendose abonar la mitad antes del comienzo del torneo y la segunda mitad antes del inicio de la segunda fecha.
El Equipo que sufra la “Expulsión del Torneo” o “se retire por propia voluntad”, NO tendrá derecho a reclamar la devolución del pago de Inscripción. dado cumplimiento a la inscripción serán considerados como “equipos excluidos del torneo por incumplimiento de la inscripción”.
</p>
<p>
Pauta 3: Costo por partido
</p>
<p>
Previo al inicio de cada disputa futbolística los equipos deberán abonar la suma dineraria de $400.
El pago del partido se deberá realizar previo al inicio de la disputa.
En caso de no haberse cumplido con ello, quedará a criterio de la Organización adoptar las medidas adecuadas al caso pudiendo resolverse la cuestión con la “quita de puntos” o “partido perdido 0 (cero) a 1 (uno) ”.
</p>
<p>
Pauta 4: Inasistencia del equipo rival.
</p>
<p>
Llegado el día del encuentro, si uno de los equipos concurre a la partida y el otro no, el equipo concurrente no deberá abonar la “planilla de juego” que equivale a $200. El equipo ausente debera abonar lo que resta para completar el costo del partido, es decir, $600.
El equipo que asistió se lo considerará ganador de la disputa con un marcador favorable de uno a cero, obteniendo como consecuencia los dos puntos. Por su parte el equipo inasistente será pasible de las siguientes medidas a adoptar por la organización conforme las circunstancias concretas del caso, entre ellas, la “quita de puntos”, “sanción económica” o “expulsión del Torneo”. Sin perjuicio de lo establecido ut supra, la organización podrá aplicar acumulativamente alguna de las medidas mencionadas anteriormente de acuerdo a las circunstancias y hechos en concreto, teniendo especial importancia la incursión en reiteradas faltas del mismo tenor.
</p>




<p>
Pauta 5: Lista de buena fe.
</p>
<p>
Antes de darse comienzo al Torneo, cada equipo deberá presentar a la organización por medio de su delegado un listado con todos sus integrantes, teniendo como mínimo 10 y como máximo 16 jugadores. Dicha lista será provista por la Organización del Torneo. La misma deberá estar integrada solamente por personas de sexo masculino de cualquier edad a partir de los 16 años cumplidos el 1 de Enero del corriente año del Torneo, no permitiéndose un mismo jugador integre diferentes listas de buena fe.
Quedan inhabilitados a integrar las Listas de Buena Fe, aquellos jugadores de equipos que hayan sido expulsados de LIGA DEL VALLE. En cada caso la habilitación individual de cada uno de los jugadores provenientes de esos equipos, podrá efectuarse previo análisis de la Organización, la cual lo determinará conforme los elementos de juicio de que disponga.
En caso de irregularidad se sancionará al jugador y al Equipo con Quita de Puntos, se dará el partido por perdido (0 a 1) y en caso de ser necesario se expulsará al Equipo del Torneo.
</p>
<p>
Pauta 6: Cambios en la lista de buena fe
</p>
<p>
En caso de que sea necesario realizar cambios de jugadores en la lista de buena fe, los mismos podrán realizarse hasta la fecha N° 3 luego de la cual no se admitirán modificaciones salvo casos de marcada excepción.
Cuando concurran circunstancias irregulares en la lista de buena fe y ameriten sanciones, las mismas tendrán el carácter de “quitas de puntos” o  “expulsión del torneo” 
Salvo lo establecido en el primer párrafo (cambio de jugadores), la lista de buena fe no podrá ser modificada bajo ningún concepto o circunstancias durante el transcurso de los campeonatos oficiales, a excepción de dos (2) lugares únicamente y que a criterio de la organización la misma considere que los motivos que obligan a dichos cambios sean fehacientes y estén debidamente comprobados. El pedido deberá cursarse por escrito mediante nota dirigida a la organización.
</p>
<p>
Pauta 7: Banco de Suplentes
</p>
<p>
El Banco de suplentes estará integrado por las personas autorizadas por la Organización (Jugadores, Director Técnico y Delegado) quedando expresamente prohibida la inclusión en el banco de suplentes de toda persona sancionada con suspensión o expulsada o simpatizantes del equipo.
Cualquier incumplimiento será motivo de advertencia o quita de puntos o partido perdido (0 a 1) o Expulsión del Torneo.
</p>





<p>
Pauta 8: Jugadores: “Identificación”
</p>
<p>
Los equipos deberán deberán presentarse con camiseta. Las camisetas deberán estar numeradas y los integrantes del plantel deberán tener números identificatorios. Hasta la fecha 3, la organización proveerá pecheras a los equipos que todavía no cuenten con sus camisetas, a partir de la cuarta fecha es obligatorio el uso de la misma.   El equipo que no cumpla con la vestimenta requerida será sancionado con la “pérdida de puntos en cada una de las presentaciones” que omita cumplir con lo requerido en esta pauta.
En caso de que el Tribunal de Disciplina detecte la inclusión de un jugador que se encuentre “INHABILITADO” para jugar debido a una sanción anteriormente impuesta que lo obligaba a no presentarse, al equipo infractor, si el tribunal lo dispone, se le dará por perdido el encuentro con un resultado de 0 (cero) a 1 (uno) en contra y la organización podrá adoptar la “la quita de puntos” o “expulsión del Torneo” del jugador que se encontraba inhabilitado y/o del equipo.
En caso de detectarse por la organización la inclusión de un jugador que no pertenece a la lista de buena fe, al equipo infractor, si el tribunal lo dispone, se le dará por perdido el encuentro con un resultado de 0 (cero) a 1 (uno) en contra en disputa y podrá ser objeto de “expulsión del Torneo”.
</p>
<p>
Pauta 9: Capitán - Comportamiento
</p>
<p>
El Capitán del Equipo deberá poseer un brazalete identificatorio. 
El Capitán, en caso de ser expulsado o informado por mal comportamiento (agresión verbal u otra acción que no contribuya a la sana competencia) será sancionado con el doble de sanción que al resto de lo jugadores por ser un referente del Equipo y del cual se espera un comportamiento ejemplar, y máxima colaboración con el normal desarrollo de la Jornada.
</p>
<p>
Pauta 10: Accesorios
</p>
<p>
Los jugadores NO podrán ingresar con accesorios (aros, pulseras, anillos, piercing, colgantes, etc.). En caso de cometer esta irregularidad, el jugador será amonestado y retirado del campo de juego hasta que regularice su situación.
</p>
<p>
Pauta 11: Forma de disputa Liga del Valle
</p>
<p>
La modalidad del torneo sera todos contra todos.
Por partido ganado se otorgaran dos (2) puntos, un (1) por empate y cero (0) por derrota.
Para la definicion de posiciones se tendra en cuenta el siguiente orden de mérito:<br>
1- Puntos Obtenidos.<br>
2- Mayor diferencia de Goles a Favor.<br>
3- Mayor cantidad de Goles a Favor.<br>
4- Menor cantidad de Goles en Contra.<br>
5- Resultado de partido entre sí a favor.<br>
6- Sorteo.<br>
</p>
<p>
Pauta 12: Tiempo de juego y Horarios
</p>
<p>
El tiempo total de cada partida a disputarse será de cincuenta (50) minutos totales. Este tiempo total se fracciona en dos periodos de tiempo de veinticinco (25) minutos cada uno con un intervalo de descanso de diez (5) minutos.
</p>
<p>
Pauta 13: Comienzo fuera del horario estipulado
</p>
<p>
Si debido a la concurrencia tardía de un equipo o los dos contendientes no se logra comenzar el partido dentro de los horarios estipulados, se restara al tiempo total la diferencia entre el horario estipulado de inicio y el real, a este resultado se lo dividirá en dos periodos que serán los que correspondan a los tiempos primero y segundo.<br>
Existe una tolerancia de 10 minutos. Una vez trascurrido este tiempo, el equipo que cumple con el horario podra hacer uso de lo antes dicho o pedir que no se juegue el encuentro, en este ultimo caso se dara por perdido el partido al equipo incumplidor. En cuanto a los costos del partido se tomara como referencia lo dicho en la pauta 4.<br>
En caso de notoria y reiterada tardanza, la organizacion podra adoptar medidas disciplinarias tales como “partido perdido por 1 (uno) a 0 (cero)”, “quita de puntos” o “expulsión del torneo”.<br>
</p>
<p>
Pauta 14: Arbitraje
</p>
<p>
Los partidos serán dirigidos por árbitros pertenecientes a ligas catamarquena de futbol, de acuerdo a la preselección que Liga Del Valle realice evaluando responsabilidad, presencia y experiencia. <br>
Las reglas del juego utilizadas por los árbitros serán las mismas que utiliza la Federación Internacional de Fútbol Asociado (FIFA) con  las particularidades detalladas en la pauta 15.<br>
Terminado el encuentro, el árbitro elaborará un informe de todo lo acontecido en el partido y expondrán sus argumentos sobre las distintas sanciones aplicadas a los participes del juego. Dichas sanciones serán comunicadas a los delegados de los equipos en la semana.<br>
</p>
<p>
Pauta 15: Particularidades del reglamento.
</p>
<p>
1) No existira el off side.<br>
2) Cuando el portero tome el balon con las manos, su saque no podra llegar mas lejos que la mitad de la cancha. En caso de que esto ocurra, el equipo adversario repondra el juego con un saque lateral desde la mitad de la cancha.<br>
</p>
<p>
Pauta 16: Derecho de Descargo
</p>
<p>
Todo equipo o persona integrante del mismo podrá ejercer su derecho a descargo sobre toda situación ante, durante o post partido. La vía para realizarlo es mediante correo electrónico a la dirección ligadelvalle2015@gmail.com. Los requisitos son: nombre, apellido y DNI de la persona que realiza el descargo y equipo al cual pertenece.<br>
La Organización, o el tribunal de disciplina, según corresponda, SOLO DARA TRATAMIENTO a los descargos o distintas solicitudes presentados los días LUNES Y MARTES posteriores a la jornada, SIN EXCEPCION.<br>
</p>

<p>
Pauta 17: Cantidad de jugadores y cambios posibles
</p>
<p>
La cantidad mínima requerida para poder disputar un partido es de seis (6) jugadores en condiciones regulares conforme con el reglamento. Los cambios a realizar son ilimitados tanto en la modalidad cambio directo como en la recambio.
</p>
<p>
Pauta 18: Ball-boy
</p>
<p>
La Organización ha dispuesto la creación de la figura “Ball-boy”. La misma tendrá la función de agilizar el juego mediante la entrega de aquellos balones que salgan del perímetro del campo. Con esta figura se logrará evitar aquellos retrasos de los partidos que lo único que logran es perjudicar el buen desempeño tanto de los participantes como de los árbitros mismos. Los mismos siendo parte de la Organización del torneo requieren del respeto de los participantes y aficionados de los equipos caso contrario a través de un informe realizado por el arbrito, el Tribunal de Disciplina sancionará a quién corresponda ya sea con “la suspensión o expulsión del/os jugador/es involucrado/s”, “la quita de puntos” o “la expulsión del Torneo” del equipo infractor.
</p>
<p>
Pauta 19: Planillas de juego
</p>
<p>
Las planillas de juego deberán ser firmadas por los jugadores, Directores Técnicos y Delegados obligatoriamente.<br>
Entregada la misma al capitán o delegado responsable de cada equipo, el mismo será responsable de la confección en su totalidad con todos los nombres de los jugadores que disputarán el encuentro. Si se llegasen a percibir errores en la planilla tales como falta de firma, de número de camiseta o número de documento de identidad, la organización adoptará las siguientes medidas: “Advertencia”, “dar por perdido el partido en disputa al equipo infractor por la mínima diferencia de 1 (uno) a 0 (cero)”, “quita de puntos” o “expulsión del torneo” de acuerdo a lo que disponga la organización.<br>
En caso de haberse cometido la falsificación de una o mas firmas, el equipo será responsable de tales acciones pudiendo ser pasible de la quita de puntos y en caso de reincidencia y a mérito del tribunal, podrá ser objeto de expulsión del torneo.<br>
</p>
<p>
Pauta 20: Delegado – Obligaciones - Comportamiento
</p>
<p>
El delegado será el responsable de cada equipo. Las obligaciones del mismo son:<br>
a) En caso de citación, asistir de manera obligatoria a las reuniones anteriores al comienzo del campeonato para conocer las novedades del torneo y cumplir con la inscripción correspondiente por equipo.<br>
b) concurrir a aquellas reuniones que sean convocadas por la organización para tratar los temas motivo de las mismas.<br>
c) recibir los llamados necesarias para cuestiones del torneo.<br>
d) recibir los mails que la organización envíe durante el torneo.<br>
e) controlar las planillas de juego una vez terminado el encuentro, pudiendo verificar junto con el árbitro quienes  hicieron los goles, los amonestados y expulsados. Los informes de los árbitros no podrán ser controlados por los delegados durante la jornada deportiva.<br>
f) El delegado deberá firmar obligatoriamente la planilla de juego previo al inicio del encuentro<br>
g) En caso de mal comportamiento será sancionado con suspensiones (prohibición de ingreso al predio donde su equipo juegue), quita de puntos o expulsión al Equipo.<br>
</p>
<p>
Pauta 21: Director Técnico – Obligaciones - Comportamiento
</p>
<p>
a) El Director Técnico deberá firmar obligatoriamente la planilla de juego previo al inicio del encuentro<br>
b) En caso de mal comportamiento será sancionado con suspensiones (prohibición de ingreso al predio donde su equipo juegue), quita de puntos o expulsión al Equipo.<br>
</p>
<p>
Pauta 22: Sanciones: procedimiento
</p>
<p>
La autoridad competente para resolver las sanciones impuestas es el “Tribunal de Disciplina de Liga Del Valle”.<br>
Todo equipo o jugador que haya sido objeto de una sanción tendrá derecho a realizar su correspondiente descargo en cuyo caso el Tribunal de Disciplina dará lugar o no a dicha solicitud.<br>
El descargo deberá ser vía mail a la dirección:ligadelvalle2015@gmail.com UNICAMENTE los días LUNES y MARTES sin EXCEPCION.<br>
En caso de ocurrir situaciones contrarias contra el buen orden, el respeto y la seguridad de los participantes, las personas representantes de la organización (ball boys, arbitros, etc.) podrán suspender el partido. Si una vez detenido no se ha podido ponerse fin al incidente. Producido ello, la comisión organizadora evaluará cuales son las medidas a adoptar con respecto al equipo infractor adoptando una de las siguientes medidas:<br>
a) dar por perdido el partido en disputa al equipo infractor por la mínima diferencia de 1 (uno) a 0 (cero)<br>
b) Quita de puntos del/los equipo/s infractor/es<br>
c) Expulsión de/los jugador/es protagonista/s de la infracción<br>
d) Expulsión del/los equipo/s al cual el/los jugador/es infractor/es pertenece/n.<br>
Los criterios para apreciar las infracciones serán: la gravedad de los incidentes; el informe del árbitro complementado con la colaboración de cualquier persona del staff de Liga del Valle.<br>
En caso de expulsión directa el jugador objeto de la medida deberá “obligatoriamente” cumplir con las fechas de sanción.<br>
En caso de expulsión por doble amarilla, si el Tribunal dispone que la sanción pertenece a la Categoría “Leve”, el jugador podrá abonando una sanción económica jugar aquella fecha en la cual se encontraba inhabilitado para jugarla (siempre que no haya tenido otro agravante), caso contrario deberá cumplir la/s fecha/s correspondiente/s. Si el jugador llegara a acumular tres (3) amarillas tendrá la opción de cumplir una (1) fecha o abonar una multa económica.<br>
En cualquier caso, la multa economica es de $30 por partido.<br>
</p>



<p>
Pauta 23: Incidentes en el predio
</p>
<p>
Todo jugador, director técnico, su ayudante o cualquier simpatizante de un equipo que tomare participación en tumultos o alteraciones del normal desarrollo de una partida que motive su suspensión será sancionado por el tribunal de disciplina. Dicha sanción podrá aplicarse a cualquier hecho incidental que se produzca antes, durante o terminada la partida, inclusive los hechos de agresión que se cometan en las inmediaciones del predio. La sanción aplicable al partícipe, irá desde la amonestación hasta la expulsión de la Liga Del Valle y podrá hacerse extensiva al equipo del cual dicha persona forme parte, pudiendo ser objeto, dicho equipo, de medidas tales como amonestación, quita de puntos y dadas las características de gravedad que revista el caso, la expulsión del torneo.
En caso de aplicar la sanción de quita de puntos, ello no obstaculiza a aplicar cualquier otra sanción que el tribunal juzgare conveniente para corregir las inconductas que la organización pretende evitar.<br>
En caso de verse suspendida una partida, su continuidad dependerá de los informes con que cuente el tribunal para juzgar los acontecimientos, pudiendo al efecto valerse de las declaraciones de los árbitro,  técnicos y cualquier persona que habiendo presenciado el hecho pueda serle útil su declaración para revelar el verdadero acontecer de los sucesos.<br>
</p>
<p>
Pauta 24: Sanciones
</p>
<p>
El Tribunal de Disciplina, está facultado para aplicar las siguientes sanciones a los equipos por faltas atribuibles
a: director técnico, ayudante de campo, suplentes de equipo, simpatizantes o allegados identificados con el equipo aún cuando no figuren en Lista de Buena Fe, que actúen en forma individual o colectiva, por conductas y actitudes contrarias a la buena educación, convivencia y principios pregonados por LIGA DEL VALLE, a saber:<br>
a) Ingreso indebido al campo de juego, durante la disputa de un partido, cualquier sea la causa – Advertencia o descuento de un (1) punto deportivo<br>
B) Consumo de bebidas alcohólicas en los bancos de sustitutos y área técnica – En caso que sea un jugador el identificado, quedará inhabilitado para participar de la disputa – sanción al Equipo: Descuento de un (1) punto deportivo.<br>
d) Ofensas y agravios verbales, provocaciones, hacia el Arbitro, Asistentes, y/o Jugadores y cuerpo técnico del equipo contrario - dos (2) puntos deportivos.<br>
e) Ofensas y agravios verbales hacia el alcanza pelota del partido – dos (2) puntos deportivos.<br>
f) En caso de prepotear, empujar y/o agredir físicamente alcanza pelota, árbitro o cualquier miembro de la Organización del Torneo se realizará advertencia ó quita de puntos ó expulsión al Equipo del Torneo.<br>
Para la mejor aplicación de las siguientes normas es que
1. Las personas que cumplan el rol de director técnico o ayudante deberán firmar la planilla de juego correspondiente al partido, de otra forma no podrán permanecer juntos a los suplentes del equipo. Como complemento a la sanción que puede ser impuesta a los equipos que dirijan, estas personas tendrán las siguientes SANCIONES PARTICULARES<br>
 1- Ante la primer acto de conducta inapropiada, serán advertidos con un correo electrónico el cual será enviado a todos los delegados.<br> 
2- de reincidir en la actitud por la que fue sancionado se le prohibirá el ingreso al predio por el tiempo que este tribunal considere adecuado en base a la falta cometida.<br>
2. Todos los jugadores suplentes deben estar uniformados y firmar la planilla de juego inmediatamente después de que terminaron de firmar los jugadores titulares. De esta forma podrán ser debidamente identificados en el caso de que incitare o participare de alteraciones del normal desarrollo del juego.<br>
3. Todas las personas que NO cumplan con el rol de jugador suplente o director técnico o delegado se deberán ubicar en el sector opuesto a la cancha del que eligió el equipo como banco suplente, de no ser así no se dará comienzo al partido. Y como se mencionó en los primeros párrafos de este comunicado normativo, toda acción en la que incurran los simpatizante o allegados que atente contra el normal desarrollo del encuentro puede ser causal de una sanción para el equipo.
Pauta 25: Sanciones Individuales<br>
a) El jugador que habiendo sido suspendido o expulsado, participe de un encuentro futbolístico oficial, será Expulsado del Torneo además de la sanción correspondiente a su Equipo.<br>
b) Aquellos jugadores que firmaren planilla de partidos y no se encuentren registrados en lista de buena fe, de no comprobarse el alta correspondiente por la Organización, serán Expulsados del Torneo además de la sanción correspondiente al Equipo.<br>
c) Aquel jugador que participe de un partido sin haber firmado la planilla de partido, será suspendido por 2 (dos) fechas.<br>
d) Aquel jugador que realice comentarios racistas y denigrantes será sancionado por el tribunal con 4 fechas de suspensión.<br>
</p>
<p>
Pauta 26: Sanciones durante la jornada de Liga Del Valle.
</p>
<p>
Aquellos Equipos (jugadores, DT, Delegados, ayudantes, simpatizantes, etc) que no colaboren con el normal desarrollo de la jornada, dentro y fuera de la cancha, por mal comportamiento, podrán ser sancionados individualmente o colectivamente con suspensiones, quita de puntos y de ser necesario expulsión del Torneo.<br>
</p>
<p>
Pauta 27: Sanción de Oficio
</p>
<p>
Aquellas personas que son informadas (independientemente de la expulsión) por el árbitro u otra autoridad del Torneo podrán recibir una sanción de oficio, informandose la misma al delegado del equipo.<br>
</p>
<p>
Pauta 28: REGLAMENTO INTERNO DEL PREDIO
</p>
<p>
El cuidado de los menores es de exclusiva responsabilidad de los padres.<br>
LIGA DEL VALLE NO ES RESPONSABLE POR ROBOS DENTRO DEL PREDIO.<br>
Deje su vehículo cerrado con llave, coloque alarma, no deje elementos de valor a la vista. RECUERDE que está en un lugar abierto, no exento de cualquier robo, daño o rotura.<br>
ESTA PROHIBIDO INGRESAR CON LOS VEHÍCULOS A LAS CANCHAS<br>
ESTA PROHIBIDO INGRESAR CON BEBIDAS QUE NO SEAN ADQUIRIDAS EN LA CANTINA DEL PREDIO.<br>
</p>
<p>
Pauta 29: Casos no contemplados
</p>
<p>
Aquellas situaciones no contempladas en el presente reglamento serán objeto de tratamiento por la organización y no podrán ser objeto de apelación por los participes del Torneo.<br>
</p>
